    // Update question card text
    dom.simQuestionText.textContent = currentQ.text;
    dom.simQuestionPagenum.textContent = `${index + 1} / ${state.questionsSequence.length}`;
    
    // Reset timers
    dom.simAnsweringTimer.textContent = formatTime(state.answeringLimit);
    dom.simAnsweringTimer.className = "timer-countdown-value";
    
