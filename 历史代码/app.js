function renderHistoryLogs() {
    const logsJson = localStorage.getItem("interview_history_logs");
    const logs = logsJson ? JSON.parse(logsJson) : [];
    
    dom.historyTableBody.innerHTML = '';
    
    if (logs.length === 0) {
        dom.historyLogsTable.style.display = "none";
        dom.historyEmptyPlaceholder.classList.add("active");
        return;
    }
    
    dom.historyLogsTable.style.display = "table";
    dom.historyEmptyPlaceholder.classList.remove("active");
    
    logs.forEach((log, index) => {
        const tr = document.createElement("tr");
        tr.innerHTML = `
            <td>${logs.length - index}</td>
            <td>${log.date}</td>
            <td><strong>${log.jobTitle}</strong></td>
            <td>${log.testModeName}</td>
            <td><span class="score-badge-table">${log.score} 分</span></td>
            <td><span class="level-badge-table">${log.matchLetter}级</span></td>
            <td>
                <button class="btn btn-secondary btn-xs btn-history-inspect" data-id="${log.id}"><i class="fa-solid fa-file-invoice"></i> 查看报告</button>
            </td>
        `;
        
        // Re-inspect click
        tr.querySelector(".btn-history-inspect").addEventListener("click", () => {
            renderReportViewData(log);
        });
        
        dom.historyTableBody.appendChild(tr);
    });
}

