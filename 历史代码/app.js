// LOCAL GRADING MODEL (Fallback)
function generateLocalMockGrading() {
    dom.loadingDetails.textContent = "本地启发式打分引擎正在评估表达条理...";
    
    // Aggregate filler counts and score calculations
    let totalScore = 0;
    let matchTotal = 0;
    let totalLength = 0;
    let totalFillers = 0;
    
    const questionsGraded = state.answersLog.map((ans, idx) => {
        const text = ans.studentAnswer;
        const len = text.length;
        totalLength += len;
        
        // Find matching keywords
        let matches = 0;
        ans.keywords.forEach(kw => {
            if (text.includes(kw)) matches++;
        });
        matchTotal += matches;
        
        // Regex match for filler words (然后, 其实, 我觉得, 就是说, 嗯)
        const filler1 = (text.match(/然后/g) || []).length;
        const filler2 = (text.match(/我觉得/g) || []).length;
        const filler3 = (text.match(/其实/g) || []).length;
        const filler4 = (text.match(/就是说/g) || []).length;
        const filler5 = (text.match(/嗯/g) || []).length;
        const fillersCount = filler1 + filler2 + filler3 + filler4 + filler5;
        totalFillers += fillersCount;
        
        // Score per question (50-98)
        let qScore = 60 + Math.min(25, matches * 6) + Math.min(15, Math.min(10, len / 75) * 1.5) - Math.min(10, fillersCount * 1.5);
        qScore = Math.max(50, Math.min(98, Math.round(qScore)));
        totalScore += qScore;
        
        return {
            title: ans.questionTitle,
            text: ans.questionText,
            score: qScore,
            verdict: len > 150 ? "要点阐述详实，能结合事业单位基本职责进行分析，逻辑结构基本完整。" : "作答篇幅略少，未能充分阐述核心步骤，内容说服力偏单薄。",
            highlight: len > 200 ? "论点清晰，能使用结构性段落词，具备较好条理性。" : "语速平稳，表达态度端正。",
            reference: ans.referenceAnswer
        };
    });
    
    // Average scores
    const avgScore = Math.round(totalScore / state.answersLog.length);
    
    // Match letter
    let matchLetter = "B";
    let matchScoreVal = 14;
    if (avgScore >= 88) { matchLetter = "A"; matchScoreVal = 18; }
    else if (avgScore >= 78) { matchLetter = "B"; matchScoreVal = 15; }
    else if (avgScore >= 68) { matchLetter = "C"; matchScoreVal = 12; }
    else { matchLetter = "D"; matchScoreVal = 9; }
    
    // Fill metric dimensions (out of 20)
    const scorePolicy = Math.min(20, Math.round(8 + Math.min(12, matchTotal * 1.8)));
    const scoreUnderstand = Math.min(20, Math.round(8 + Math.min(12, totalLength / 180)));
    const scoreLogic = Math.min(20, Math.round(9 + Math.min(11, (state.answersLog[0].studentAnswer.match(/(第一|首先|二是|其次|最后)/g) || []).length * 3)));
    const scoreExpression = Math.min(20, Math.round(20 - Math.min(10, totalFillers * 0.5)));
    const scoreManner = Math.min(20, Math.round(13 + Math.random() * 3));
    
    const finalReport = {
        score: avgScore,
        verdict: avgScore >= 85 ? "匹配度极佳，表达有条理，符合岗位要求" : "基本胜任岗位，逻辑清晰，时政素养有待积累",
        matchLetter: matchLetter,
        matchScore: matchScoreVal,
        stableScore: Math.min(20, Math.round(avgScore * 0.2)),
        scores: {
            policy: scorePolicy,
            logic: scoreLogic,
            practice: scoreUnderstand, // matching
            expression: scoreExpression,
            understand: scoreUnderstand,
            manner: scoreManner
        },
        strengths: [
            { title: "逻辑框架感较强", description: "作答能划分小标题分点阐述（如一是、二是），层层推进，具有符合结构化要求的骨架。" },
            { title: "岗位认识较为贴合", description: `言语中提到了【${state.jobKeywords.slice(0, 2).join("、")}】等岗位要素，展现了对单位职责的认知。` }
        ],
        weaknesses: [
            { title: "内容论据缺乏实例支撑", description: "表达多停留于纯理论层面的表态，未能辅以具体的项目协调数据或过往实践经验，略显空泛。" },
            { title: "口头禅频率偏高", description: `在说话间隙，无意中使用了过多“然后”、“我觉得”等口头修饰词，打乱了考官听题节奏。` }
        ],
        fillers: {
            f1: Math.min(9, Math.round(totalFillers * 0.3)),
            f2: Math.min(9, Math.round(totalFillers * 0.2)),
            f3: Math.min(9, Math.round(totalFillers * 0.15)),
            f4: Math.min(9, Math.round(totalFillers * 0.1)),
            f5: Math.min(9, Math.round(totalFillers * 0.25))
        },
        jdAnalysis: state.jobKeywords.map(kw => ({
            title: kw,
            desc: `在回答中基本融入了对${kw}的思考，能够说明候选人具备这方面的专业意识。`
        })),
        overallVerdict: `候选人作答态度诚恳，具备较好的综合能力基础。针对报考的【${state.jobTitle}】，言语逻辑性已达标。若能在政策高度上进一步提炼，并改掉口头重复副词的习惯，竞争力将显著提高。`,
        questions: questionsGraded
    };
    
    compileAndSaveReport(finalReport);
}

// DEEP GEMINI SCORING
async function callGeminiAPIGrading(apiKey) {
    dom.loadingDetails.textContent = "正在向Gemini API发送联考评估Prompt...";
    
    // Construct questions segment
    const questionsPromptData = state.answersLog.map((ans, idx) => `
    【第 ${idx + 1} 题】：${ans.questionText}
    【考生作答】：${ans.studentAnswer || "（未录音转录或考生选择跳过）"}
    【官方参考范文】：${ans.referenceAnswer}
    -----------------`).join('\n');
    
    const mainPrompt = `您是上海市事业单位面试的首席主考官。请根据考生的报考岗位、职责关键词及个人简历亮点，对考生在本次连考中的表现进行综合、细致的打分和多维度AI评估。
    
    【报考岗位】：${state.jobTitle}
    【报考单位/系统】：${state.jobUnit}
    【岗位核心要求】：${state.jobKeywords.join("、")}
    【岗位职责JD说明】：${state.jobJD}
    【考生亮点简历背景】：${state.jobExperience}
    
    考生在本次三题模拟连考中的实录情况如下：
    ${questionsPromptData}
    
    请严格遵循以下JSON格式输出评估结果，不可包含任何 markdown 块标记（即不要包围在 \`\`\`json 内），确保整体输出能够直接被 JS 的 JSON.parse 解析。
    
    输出JSON结构模板：
    {
      "score": 84, // 综合得分，满分100，建议在 50-95 之间
      "verdict": "表现良好，逻辑清晰", // 简短总评字数在15字以内
      "matchLetter": "A", // 人岗匹配度评级，A, B, C, 或 D
      "matchScore": 18, // 人岗匹配度评分，满分20分
      "stableScore": 16, // 表达稳定度评分，满分20分
      "scores": {
        "policy": 18, // 政策理解度分值，满分20分
        "logic": 16, // 逻辑条理性分值，满分20分
        "practice": 16, // 实践可行性分值，满分20分
        "expression": 15, // 言语表达力分值，满分20分
        "understand": 15, // 岗位理解分值，满分20分
        "manner": 14 // 仪态与稳定度分值，满分20分
      },
      "strengths": [
        { "title": "优点一", "description": "具体分析描述..." },
        { "title": "优点二", "description": "具体分析描述..." }
      ],
      "weaknesses": [
        { "title": "缺点一", "description": "具体分析描述..." },
        { "title": "缺点二", "description": "具体分析描述..." }
      ],
      "fillers": {
        "f1": 5, // “然后”口头禅出现的预估次数
        "f2": 2, // “我觉得”口头禅次数
        "f3": 3, // “其实”口头禅次数
        "f4": 1, // “就是说”次数
        "f5": 4  // “嗯/呃”等语气助词次数
      },
      "jdAnalysis": [
        // 对前面填入的每一个“岗位职责关键词”，分析考生在答题中是否有所体现
        { "title": "沟通协调", "desc": "结合考生回答分析..." }
      ],
      "overallVerdict": "总体评价，结合岗位要求给出考官寄语...",
      "questions": [
        // 分别对应回答的各道题，包含各题得分、简短评语与亮点
        { "title": "第一题标题", "text": "第一题原题干", "score": 82, "verdict": "第一题简评...", "highlight": "第一题亮点...", "reference": "参考答案内容" }
      ]
    }`;
    
    try {
        const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${apiKey}`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({
                contents: [{
                    parts: [{
                        text: mainPrompt
                    }]
                }]
            })
        });
        
        if (!response.ok) throw new Error("Gemini API request failed");
        
        const data = await response.json();
        let rawText = data.candidates[0].content.parts[0].text.trim();
        
        // Strip markdown decorators
        if (rawText.startsWith("```")) {
            rawText = rawText.replace(/^```json\s*/i, "").replace(/```$/, "").trim();
        }
        
        const reportJson = JSON.parse(rawText);
        compileAndSaveReport(reportJson);
        
    } catch (e) {
        console.error("Gemini grading failed:", e);
        showToast("Gemini AI评分接口调用失败，自动为您加载本地智能批改系统...", "warning");
        generateLocalMockGrading();
    }
}

// RENDER & PERSIST REPORT CARD
function compileAndSaveReport(report) {
    dom.loadingOverlay.classList.remove("active");
    
    // Save to LocalStorage
    const logsJson = localStorage.getItem("interview_history_logs");
    let logs = logsJson ? JSON.parse(logsJson) : [];
    
    const formattedDate = new Date().toLocaleString('zh-CN', { hour12: false });
    
    const newLogItem = {
        id: "log_" + Date.now(),
        date: formattedDate,
        jobTitle: state.jobTitle,
        testModeName: state.testMode === "full-test" ? "整套三题模拟" : "单题专项模拟",
        score: report.score,
        matchLetter: report.matchLetter,
        reportDetails: report
    };
    
    logs.unshift(newLogItem);
    localStorage.setItem("interview_history_logs", JSON.stringify(logs));
    
    // Render current report
    renderReportViewData(newLogItem);
}

