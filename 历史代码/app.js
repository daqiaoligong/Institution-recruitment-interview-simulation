// PURE CANVAS SPARKLINE TREND CHART
function drawSparklineTrends() {
    const canvas = dom.canvasSparkline;
    if (!canvas) return;
    
    const ctx = canvas.getContext("2d");
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    
    // Read previous mock scores
    const logsJson = localStorage.getItem("interview_history_logs");
    if (!logsJson) return;
    
    const logs = JSON.parse(logsJson).slice(0, 6).reverse(); // max 6 items
    if (logs.length < 2) {
        // Draw a simple mock line if just one test
        ctx.beginPath();
        ctx.moveTo(10, 25);
        ctx.lineTo(210, 15);
        ctx.strokeStyle = "rgba(47, 84, 235, 0.4)";
        ctx.lineWidth = 2;
        ctx.stroke();
        return;
    }
    
    const step = 200 / (logs.length - 1);
    
    // Calculate coordinates
    const points = logs.map((item, idx) => {
        const x = 10 + idx * step;
        // map score 50-100 to y-coords (35 to 5)
        const y = 35 - ((item.score - 50) / 50) * 30;
        return { x, y, score: item.score };
    });
    
    // Draw background filling under line
    ctx.beginPath();
    ctx.moveTo(points[0].x, 38);
    points.forEach(pt => ctx.lineTo(pt.x, pt.y));
    ctx.lineTo(points[points.length - 1].x, 38);
    ctx.closePath();
    ctx.fillStyle = "rgba(47, 84, 235, 0.08)";
    ctx.fill();
    
    // Draw trend line
    ctx.beginPath();
    ctx.moveTo(points[0].x, points[0].y);
    for (let i = 1; i < points.length; i++) {
        ctx.lineTo(points[i].x, points[i].y);
    }
    ctx.strokeStyle = "var(--primary-color)";
    ctx.lineWidth = 2.5;
    ctx.stroke();
    
    // Add dot points
    points.forEach(pt => {
        ctx.beginPath();
        ctx.arc(pt.x, pt.y, 4, 0, 2 * Math.PI);
        ctx.fillStyle = "#ffffff";
        ctx.fill();
        ctx.strokeStyle = "var(--primary-color)";
        ctx.lineWidth = 1.5;
        ctx.stroke();
    });
}

