function showToast(message, type = "info") {
    const toast = document.createElement("div");
    toast.className = `btn btn-secondary`;
    toast.style.background = "var(--bg-card)";
    toast.style.color = "var(--text-main)";
    toast.style.borderLeft = `4px solid ${type === 'error' ? 'var(--color-danger)' : type === 'success' ? 'var(--color-success)' : 'var(--primary-color)'}`;
    toast.style.animation = "fadeIn 0.3s ease";
    toast.style.fontSize = "0.85rem";
    toast.style.padding = "8px 16px";
    toast.style.boxShadow = "var(--shadow-floating)";
    
    const icon = document.createElement("i");
    icon.className = type === 'error' ? 'fa-solid fa-circle-xmark' : type === 'success' ? 'fa-solid fa-circle-check' : 'fa-solid fa-circle-info';
    icon.style.color = type === 'error' ? 'var(--color-danger)' : type === 'success' ? 'var(--color-success)' : 'var(--primary-color)';
    icon.style.marginRight = "8px";
    
    toast.appendChild(icon);
    toast.appendChild(document.createTextNode(message));
    dom.toastContainer.appendChild(toast);
    
    setTimeout(() => {
        toast.style.opacity = "0";
        toast.style.transform = "translateY(8px)";
        toast.style.transition = "opacity 0.4s ease, transform 0.4s ease";
        setTimeout(() => toast.remove(), 400);
    }, 3200);
}

